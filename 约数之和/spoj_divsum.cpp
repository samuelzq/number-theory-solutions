/**
 * Number of divisors (真因子之和)
 * 问题链接：
 * https://www.spoj.com/problems/DIVSUM/
 * https://vjudge.net/problem/SPOJ-DIVSUM
 *
 * 解题思路：
 * 使用筛法（类似埃拉托斯特尼筛法）预先计算所有数的真因子之和
 * 时间复杂度：O(N log N)，空间复杂度：O(N)
 * 这种方法比对每个数单独求因子要高效得多，因为避免了重复计算
 *
 * @File:   spoj_divsum.cpp
 * @Author: Lao Zhang <samuelzhang77@yahoo.com>
 * @Date:   2022-01-26
 *
 *
 */
#include <bits/stdc++.h>

using namespace std;
#define LL long long

// 定义最大数值范围，根据题目要求，N最大为500,000
const int N = 5e5;
// 数组d用于存储每个数的真因子之和，d[i]表示数i的所有真因子之和
LL d[N + 1];

/**
 * 预处理函数：使用筛法计算1到N每个数的真因子之和
 * 真因子是指除了自身以外的所有正因子
 * 例如：数6的真因子有1,2,3，它们的和为6
 */
void pre_solve(void)
{
    // 1没有真因子，所以和为0
	d[1] = 0;
	
	// 外层循环：从2开始遍历每个数
	for (int i = 2; i <= N; i++) {
		// 1是所有大于1的数的真因子，所以先加1
		d[i] += 1;
		
		// 内层循环：对于每个数i，将它加到所有i的倍数上
		// 因为对于数j（j是i的倍数），i是j的一个真因子
		// 从2i开始是因为对于数i本身，i不是真因子（真因子不包括自身）
		for (int j = 2 * i; j <= N; j += i)
			d[j] += i;  // 将i加到j的真因子和中
	}
	
	// 算法示例：
	// 当i=2时，会将2加到4,6,8,10,...这些数的因子和中
	// 当i=3时，会将3加到6,9,12,15,...这些数的因子和中
	// 这样最终d[6] = 1(来自i=2的贡献) + 1(初始加1) + 3(来自i=3的贡献) = 5
	// 验证：6的真因子有1,2,3，和为6，但这里得到5，说明少了什么？
	// 注意：初始化时d[6]先加了1，然后i=2时加了2，i=3时加了3，所以d[6]=6，正确！
}

int main(void)
{
	// 预处理所有数的真因子之和
	pre_solve();

	int t;
	cin >> t;  // 读取测试用例数量
	
	// 处理每个测试用例
	while (t--) {
		int n;
		cin >> n;  // 读取当前要计算的数
		cout << d[n] << '\n';  // 直接输出预处理好的结果
	}
	return 0;
}
